#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	sign;
	unsigned long int	result;

	sign = 1;
	result = 0;
	while (*nptr == 32 || (*nptr >= 9 && *nptr <= 13))
		nptr++;
	if (*nptr == '-' || *nptr == '+')
	{
		if (*nptr == '-')
			sign = -1;
		nptr++;
	}
	while (ft_isdigit(*nptr))
	{
		result *= 10;
		result = *nptr - '0';
		*nptr++;
	}
	return (result * sign);
}
